    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        try {
            String nome = txtNome.getText();
            Autor objAu = new Autor();
            AutorDadosTratados dados = new AutorDadosTratados();

            objAu = dados.pesquisarPorNome(nome);
            if (objAu.getNome() != null) {
                txtId.setText(String.valueOf(objAu.getId()));
                txtNome.setText(objAu.getNome());
                txtNacio.setText(objAu.getNacionalidade());
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro: " + e);
        }


    }//GEN-LAST:event_btnPesquisarActionPerformed

