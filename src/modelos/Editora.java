package modelos;

/**
 *
 * @author rober
 */
public class Editora {
    
    private int id;
    private String nome;
    private String CNPJ;
    
    //getters e setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCNPJ() {
        return CNPJ;
    }

    public void setCNPJ(String CNPJ) {
        this.CNPJ = CNPJ;
    }
    
    public String toString(){
        return this.nome;
    }    
    
}
